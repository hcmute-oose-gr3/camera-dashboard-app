<script lang="ts">
	import { onMount } from 'svelte';
	let div: HTMLDivElement;
	onMount(async () => {
		const ApexCharts = await import('apexcharts').then((r) => r.default);
		const chart = new ApexCharts(div, {
			series: [
				{
					data: [2.3, 3.1, 4.0, 10.1, 4.0, 3.6, 3.2, 2.3, 1.4, 0.8, 0.5, 0.2],
				},
			],
			chart: {
				height: 500,
				type: 'bar',
			},
			plotOptions: {
				bar: {
					borderRadius: 10,
					dataLabels: {
						position: 'top', // top, center, bottom
					},
				},
			},
			dataLabels: {
				enabled: true,
				formatter: function (val: number) {
					return val + '%';
				},
				offsetY: -20,
				style: {
					fontSize: '12px',
					colors: ['#304758'],
				},
			},

			xaxis: {
				categories: [
					'Jan',
					'Feb',
					'Mar',
					'Apr',
					'May',
					'Jun',
					'Jul',
					'Aug',
					'Sep',
					'Oct',
					'Nov',
					'Dec',
				],
				position: 'top',
				axisBorder: {
					show: false,
				},
				axisTicks: {
					show: false,
				},
				crosshairs: {
					fill: {
						type: 'gradient',
						gradient: {
							colorFrom: '#D8E3F0',
							colorTo: '#BED1E6',
							stops: [0, 100],
							opacityFrom: 0.4,
							opacityTo: 0.5,
						},
					},
				},
				tooltip: {
					enabled: true,
				},
			},
			yaxis: {
				axisBorder: {
					show: false,
				},
				axisTicks: {
					show: false,
				},
				labels: {
					show: false,
					formatter: function (val: number) {
						return val + '%';
					},
				},
			},
			title: {
				text: 'Chart Warning',
				floating: true,
				offsetY: 510,
				align: 'center',
				style: {
					color: '#444',
				},
			},
		});
		chart.render();
	});
</script>

<div />
<div bind:this={div} />
